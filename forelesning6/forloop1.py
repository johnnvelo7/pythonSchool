#!/usr/bin/env python3

course = "PGR107"

# WHILE LOOP IN PYTHON
i = 0
while i < len(course):
    print(course[i])
    i += 1

# FOR LOOP IN PYTHON
for letter in course:
    print(letter)

# PRINT 1 TO 9
for i in range(1, 10):
    print(i)

# PRINT 1 TO 9 IN WHILE LOOP
i = 1
while i < 10:
    print(i)
    i = i + 1


#  FOR LOOP, THIRD ARGUMENT IS A STEP
for i in range(1, 10, 2):
    print(i)

# FOR LOOP, 10 TO 0
for i in range(10, 0, -1):
    print(i)

